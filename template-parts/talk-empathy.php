<p>If you want to build websites that people love, you need to understand those people. But what happens if you're just not a people person?</p>

<p>Don’t worry: empathy is a skill that anyone can learn. It just takes a little practise. Approaching design from an inclusive perspective has lead to some of the most innovative designs we use every day, while a lack of empathy can lead to catastrophic failures. Let's learn how to see diversity and disability as opportunities—not obstacles.</p>

<p>By putting ourselves in our users’ shoes, we can make the web a better place for everyone.</p>

