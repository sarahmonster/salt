<p>It doesn't matter how brilliant your code is or how beautiful your design is—if your visitors can't use your website, it's useless. A website that's designed with your users in mind is the best way to achieve your goals—but how do we go about building websites that users love?</p>

<p>In this talk, we'll take a look at the importance of website usability and we'll offer up some practical advice for ways to improve your own site. We'll also give an introduction to usability testing, along with ideas for low-friction implementation. Along the way, we'll look at lots of examples of websites that get it wrong, and a few that do it really well.</p>

<p>You'll leave with lots of ideas to help make your websites more effective.</p>

