<p>Why do so many websites look the same? As our tools have improved and we’ve been able to do more with the web, there’s been a growing trend toward websites that look exactly alike. Why did this come to be, and what can we, as designers and curators of the modern web, do to avoid it?</p>

<p>In order to answer these questions, we’ll touch briefly on the history of web design, and examine how modern tools and practises have contributed to a more homogeneous-looking internet. We’ll examine the web design process from two perspectives – the designer’s and the developer’s – and discuss how the blurring of those roles impacts the design process. Along the way, we’ll discuss the various merits of following or bucking trends. Finally, I’ll offer some constructive advice to help you – whether you’re a designer or not – add an individual touch to your design work.</p>

