<p>The line between design and development is getting blurrier, and our approach to web design is becoming more modular. How do we keep track of it all?</p>

<p><em>Style guides to the rescue!</em></p>

<p>Using style guides that update as our projects progress, we can visually document our work and ensure that everything stays consistent. They may seem daunting and time-consuming at first, but they don’t have to be.</p>

<p>How do I get started with living style guides? What’s a living style guide anyway, and how do I know if mine is dead? Isn’t that going to slow me down? Why should I bother?</p>

<p>Let’s find out.</p>


